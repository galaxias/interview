package algo.dfs;

public class Road {


    private int d;//终点
    private int L;//长度
    private int t;//过路费

    public int getL() {
        return L;
    }

    public void setL(int l) {
        L = l;
    }

    public int getT() {
        return t;
    }

    public void setT(int t) {
        this.t = t;
    }

    public int getD() {
        return d;
    }

    public void setD(int d) {
        this.d = d;
    }
}
